#include <iostream>

int main() {
    // Declarar variables para almacenar los tres números
    double num1 = 53;
    double num2 = -27;
    double num3 = 0.005;
    double suma;

    // Calcular la suma
    suma = num1 + num2 + num3;

    // Mostrar el resultado
    std::cout << "La suma de los tres números es: " << suma << std::endl;

    return 0;
}